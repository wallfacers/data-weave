-- 文件夹类目树：只存文件夹节点；任务/工作流经 catalog_node_id 回指挂入。
-- 邻接表 parent_id（父子关系，根节点为空）+ 物化路径 path（形如 /1/4/9/，含首尾 /），
-- 支撑子树 LIKE 前缀查询、面包屑拆串、防环判定。path 为后端派生字段（REST 不接受）。
CREATE TABLE catalog_node (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id   BIGINT NOT NULL,
    project_id  BIGINT NOT NULL,
    parent_id   BIGINT,                        -- 父文件夹（NULL=根）
    name        VARCHAR(256) NOT NULL,
    path        VARCHAR(2000) NOT NULL,        -- 物化路径 /id/.../id/（含首尾 /）
    sort_order  INTEGER,                       -- 同级排序（NULL 视为 0）
    created_by  BIGINT,
    updated_by  BIGINT,
    created_at  TIMESTAMP,
    updated_at  TIMESTAMP,
    deleted     SMALLINT DEFAULT 0,
    version     INTEGER DEFAULT 0
);
CREATE INDEX IF NOT EXISTS idx_catalog_node_parent ON catalog_node (parent_id);
CREATE INDEX IF NOT EXISTS idx_catalog_node_path ON catalog_node (path);
CREATE INDEX IF NOT EXISTS idx_catalog_node_project ON catalog_node (project_id);
CREATE INDEX IF NOT EXISTS idx_task_def_catalog ON task_def (catalog_node_id);
CREATE INDEX IF NOT EXISTS idx_workflow_def_catalog ON workflow_def (catalog_node_id);

