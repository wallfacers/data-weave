   SET project_id = (SELECT MIN(p.id) FROM projects p WHERE p.tenant_id = alert_channel.tenant_id AND p.deleted = 0)
 WHERE project_id IS NULL;

UPDATE alert_route
   SET project_id = (SELECT MIN(p.id) FROM projects p WHERE p.tenant_id = alert_route.tenant_id AND p.deleted = 0)
 WHERE project_id IS NULL;

UPDATE quality_rule
   SET project_id = (SELECT MIN(p.id) FROM projects p WHERE p.tenant_id = quality_rule.tenant_id AND p.deleted = 0)
 WHERE project_id IS NULL;

UPDATE quality_check_run
   SET project_id = (SELECT MIN(p.id) FROM projects p WHERE p.tenant_id = quality_check_run.tenant_id AND p.deleted = 0)
