-- ============================================================
-- 036 项目隔离回填（幂等）：存量数据 project_id 为 NULL 时，回填到该租户的默认项目
-- （取该租户 projects 表中最小的 id）。仅在 mode=always 非 DROP+CREATE 的增量迁移场景生效；
-- DROP+CREATE 场景下表已空，UPDATE 零行影响，无害。
-- ============================================================
UPDATE alert_rule
   SET project_id = (SELECT MIN(p.id) FROM projects p WHERE p.tenant_id = alert_rule.tenant_id AND p.deleted = 0)
 WHERE project_id IS NULL;

