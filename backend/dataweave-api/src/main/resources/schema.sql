-- 节点级 DAG 冻结 overlay（ops-center-publish-boundary）：运维侧临时止血，键 (workflow_id, node_key, instance_id)。
-- instance_id 为空=定义级（此后每个 cron 物化实例都跳该节点）；非空=实例级（仅该实例该节点）。
-- overlay 不写入发布快照 dag_snapshot_json，调度物化时叠加；冻结节点及其传递下游（含弱依赖边）SKIPPED。
CREATE TABLE workflow_node_freeze (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id    BIGINT NOT NULL,
    project_id   BIGINT NOT NULL,
    workflow_id  BIGINT NOT NULL,
    node_key     VARCHAR(64) NOT NULL,
    instance_id  UUID,                          -- 空=定义级；非空=该工作流实例级
    frozen       SMALLINT DEFAULT 1,            -- 1=冻结（解冻时删行或置 0）
    created_by   BIGINT,
    updated_by   BIGINT,
    created_at   TIMESTAMP,
    updated_at   TIMESTAMP,
    deleted      SMALLINT DEFAULT 0,
    version      INTEGER DEFAULT 0
);
-- 物化叠加查询：按 (workflow_id, instance_id) 取该工作流定义级 + 该实例实例级冻结节点。
CREATE INDEX IF NOT EXISTS idx_node_freeze_lookup ON workflow_node_freeze (workflow_id, instance_id, deleted);

