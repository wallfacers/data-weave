    biz_date     VARCHAR(32),
    total_tasks     INTEGER DEFAULT 0,       -- 总节点数
    completed_tasks INTEGER DEFAULT 0,       -- 已完成节点数
    failed_tasks    INTEGER DEFAULT 0,       -- 失败节点数
